let weather = 'sunny';

switch (weather) {
  case 'sunny':
    console.log('It\s a beautiful day!');
    break;
  case 'rainy':
    console.log('Grab your umbrella.');
    break;
  case 'storm':
    console.log('Close the windows.');
    break
  case 'snowstorm':
    console.log('Be prepared for the snow.');
    break
  default:
    console.log('Let\'s stay inside.');
}